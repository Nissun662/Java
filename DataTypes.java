
public class DataTypes {
    public static void main(String[] args) 
    {
        int num1 = 5;
        float num2 = 10.5F; //need f/F in the end cuz by default it's double
        byte by = 127; //byte range is between -128 to 127
        short sh = 558; 
        long l = 5854l;
        double d = 10.5;
        char c = 'K';
        boolean b = true;
    }
    /* we define various data types as per our needs.
    If we need a integer of value 127 (in the range for byte),
    then we can use byte while defining variable to
    save memory because it only uses 1byte/8bits of memory
    */
}